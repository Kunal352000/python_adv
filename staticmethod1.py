class Test:
    @staticmethod
    def add(x,y):
        print("The sum of x and y =",x+y)
    @staticmethod
    def sub(x,y):
        print("The sub of x and y =",x-y)
    @staticmethod
    def average(x,y):
        print("The average value:",(x+y)/2)
Test.add(10,20)
Test.sub(10,20)
Test.average(10,20)
